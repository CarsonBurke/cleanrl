# PPO Tanh + Tanh-squashed Gaussian + SPO-asym half-strength.
#
# Sibling to pmpo_d4_beta_spo_asym_halfstrength_v1 — same SPO-asym objective
# and same half-strength bounds (ε_low=0.40, ε_high=0.56). Backbone swaps
# Beta → bounded Gaussian; ReluSq → Tanh activations.
#
# Action distribution: a = tanh(z),  z ~ Normal(μ(s), σ),  σ = exp(logstd).
# Bounding via tanh (not env-side ClipAction) avoids the well-known
# Normal+clip mismatch where the executed action differs from the one
# whose log-prob enters the policy gradient. The Jacobian of tanh is
# folded into the log-prob:
#   log p(a|s) = Σ_i [ log N(z_i; μ_i, σ) − log(1 − tanh(z_i)² + ε) ]
# Stored actions are post-tanh; on re-evaluation we recover z by atanh
# of the clamped action.
#
#   J(θ) = E[ r·A − (|A|/(2·ε(A,r))) · (r − 1)² ]
#   ε(A, r) = ε_high  if sign(A) == sign(r − 1)
#             ε_low   otherwise
#
# Half-strength: bounds doubled vs the asym defaults (0.20/0.28 → 0.40/0.56),
# which halves the SPO penalty coefficient |A|/(2ε) and permits more drift.
